// Printify„Çµ„Ç§„Ç∫Âà•‰æ°Ê†º‰∏ÄÊã¨Êõ¥Êñ∞Ôºà2XL/3XLÂØæÂøú„ÄÅ38%Âà©ÁõäÁéáÈÅîÊàêÔºâ
import { rateLimitMiddleware } from '../lib/rateLimiter.js';
import { asyncHandler, validateRequired, validateEnv, ExternalAPIError } from '../lib/errorHandler.js';

async function handler(req, res) {
    if (req.method !== 'POST') {
        return res.status(405).json({ error: 'Method not allowed' });
    }

    validateEnv(['PRINTIFY_API_KEY']);
    validateRequired(req.body, ['shopId']);

    const { shopId, targetMargin = 38, dryRun = false, productIds = null } = req.body;
    const apiKey = process.env.PRINTIFY_API_KEY;

    // Blueprint ID„Åî„Å®„ÅÆÂéü‰æ°„Éû„ÉÉ„Éî„É≥„Ç∞
    const blueprintCosts = {
        6: { baseCost: 900, extraCost: { '2XL': 1200, '3XL': 1500 }, name: 'Gildan 5000 T-Shirt' },
        26: { baseCost: 1050, extraCost: { '2XL': 1350, '3XL': 1650 }, name: 'Gildan 980 Lightweight Tee' },
        36: { baseCost: 1200, extraCost: { '2XL': 1500, '3XL': 1800 }, name: 'Gildan 2000 Ultra Cotton Tee' },
        145: { baseCost: 1050, extraCost: { '2XL': 1350, '3XL': 1650 }, name: 'Gildan 64000 Softstyle T-Shirt' },
        157: { baseCost: 750, extraCost: {}, name: 'Gildan 5000B Kids Tee' },
        80: { baseCost: 1350, extraCost: { '2XL': 1650, '3XL': 1950 }, name: 'Gildan 2400 Long Sleeve Tee' },
        49: { baseCost: 2100, extraCost: { '2XL': 2550, '3XL': 3000 }, name: 'Gildan 18000 Sweatshirt' },
        77: { baseCost: 2550, extraCost: { '2XL': 3000, '3XL': 3450 }, name: 'Gildan 18500 Hoodie' }
    };

    // USD $X.99 ‰æ°Ê†ºË®àÁÆóÈñ¢Êï∞Ôºà38%ÂâçÂæå„ÅÆÂà©ÁõäÁéáÔºâ
    const JPY_TO_USD = 150; // 1 USD = 150 JPY
    const calculateOptimalPrice = (costJpy, targetMargin) => {
        // ÂÜÜ‚Üí„Éâ„É´Â§âÊèõ
        const costUsd = costJpy / JPY_TO_USD;
        // ÁõÆÊ®ô‰æ°Ê†º„ÇíË®àÁÆó
        const exactPriceUsd = costUsd / (1 - targetMargin / 100);
        // Ê¨°„ÅÆ$X.99„Å´Âàá„Çä‰∏ä„Åí
        const priceUsd = Math.ceil(exactPriceUsd) - 0.01;
        // Printify API„ÅØ„Çª„É≥„ÉàÂçò‰ΩçÔºàÊï¥Êï∞Ôºâ„Åß‰æ°Ê†º„ÇíÂèó„ÅëÂèñ„Çã
        return Math.round(priceUsd * 100);
    };

    try {
        console.log(`üìä „Çµ„Ç§„Ç∫Âà•‰æ°Ê†º‰∏ÄÊã¨Êõ¥Êñ∞ÈñãÂßã: ÁõÆÊ®ôÂà©ÁõäÁéá${targetMargin}%`);
        if (dryRun) {
            console.log('‚ö†Ô∏è DRY RUN„É¢„Éº„Éâ: ÂÆüÈöõ„ÅÆÊõ¥Êñ∞„ÅØË°å„ÅÑ„Åæ„Åõ„Çì');
        }
        if (productIds) {
            console.log(`üéØ ÁâπÂÆöÂïÜÂìÅ„ÅÆ„ÅøÊõ¥Êñ∞: ${productIds.length}ÂïÜÂìÅ`);
        }

        let products = [];

        // ÁâπÂÆöÂïÜÂìÅID„ÅåÊåáÂÆö„Åï„Çå„Å¶„ÅÑ„ÇãÂ†¥Âêà„ÅØ„ÄÅ„Åù„Çå„Çâ„ÅÆ„ÅøÂá¶ÁêÜ
        if (productIds && Array.isArray(productIds) && productIds.length > 0) {
            // productIds „Åã„ÇâÂïÜÂìÅÊÉÖÂ†±„ÇíÁõ¥Êé•‰ΩúÊàêÔºàË©≥Á¥∞ÂèñÂæó„ÅØÂæå„ÅßË°å„ÅÜÔºâ
            products = productIds.map(id => ({ id }));
            console.log(`üìã ${products.length}ÂïÜÂìÅ„ÇíÂØæË±°„Å´Ë®≠ÂÆö`);
        } else {
            // ÂÖ®ÂïÜÂìÅ„ÇíÂèñÂæóÔºà„Éö„Éº„Ç∏„Éç„Éº„Ç∑„Éß„É≥ÂØæÂøúÔºâ
            let allProducts = [];
            let currentPage = 1;
            let hasMorePages = true;

            while (hasMorePages) {
                const productsResponse = await fetch(
                    `https://api.printify.com/v1/shops/${shopId}/products.json?limit=50&page=${currentPage}`,
                    {
                        method: 'GET',
                        headers: {
                            'Authorization': `Bearer ${apiKey}`,
                            'Content-Type': 'application/json'
                        }
                    }
                );

                if (!productsResponse.ok) {
                    const errorText = await productsResponse.text();
                    throw new ExternalAPIError('Printify', `Failed to fetch products (${productsResponse.status})`, errorText);
                }

                const productsData = await productsResponse.json();
                const pageProducts = productsData.data || [];
                allProducts = allProducts.concat(pageProducts);

                console.log(`üìÑ „Éö„Éº„Ç∏${currentPage}: ${pageProducts.length}‰ª∂ÂèñÂæó`);

                hasMorePages = pageProducts.length === 50;
                currentPage++;

                if (hasMorePages) {
                    await new Promise(resolve => setTimeout(resolve, 300));
                }
            }

            products = allProducts;
            console.log(`üìã ${products.length}ÂïÜÂìÅ„ÇíÂèñÂæó`);
        }

        let updatedCount = 0;
        let skippedCount = 0;
        let errorCount = 0;
        const updateDetails = [];

        // ÂêÑÂïÜÂìÅ„ÇíÂá¶ÁêÜ
        for (const product of products) {
            try {
                console.log(`\nÂá¶ÁêÜ‰∏≠: ${product.title} (ID: ${product.id})`);

                // ÂïÜÂìÅË©≥Á¥∞„ÇíÂèñÂæó
                const detailResponse = await fetch(
                    `https://api.printify.com/v1/shops/${shopId}/products/${product.id}.json`,
                    {
                        method: 'GET',
                        headers: {
                            'Authorization': `Bearer ${apiKey}`,
                            'Content-Type': 'application/json'
                        }
                    }
                );

                if (!detailResponse.ok) {
                    console.error(`Failed to fetch product ${product.id}`);
                    errorCount++;
                    continue;
                }

                const detail = await detailResponse.json();
                const blueprintId = detail.blueprint_id;
                const variants = detail.variants || [];

                const costInfo = blueprintCosts[blueprintId];
                if (!costInfo) {
                    console.log(`Unknown blueprint ${blueprintId}, skipping`);
                    skippedCount++;
                    updateDetails.push({
                        productId: product.id,
                        title: product.title,
                        status: 'skipped',
                        reason: `Unknown blueprint: ${blueprintId}`
                    });
                    continue;
                }

                // ÂêÑvariant„Å´ÊúÄÈÅ©‰æ°Ê†º„ÇíË®≠ÂÆö
                const updatedVariants = variants.map(variant => {
                    const variantTitle = variant.title || '';
                    let cost = costInfo.baseCost;

                    // „Çµ„Ç§„Ç∫„ÇíÊ§úÂá∫
                    if (variantTitle.includes('2XL')) {
                        cost = costInfo.extraCost['2XL'] || costInfo.baseCost * 1.33;
                    } else if (variantTitle.includes('3XL')) {
                        cost = costInfo.extraCost['3XL'] || costInfo.baseCost * 1.67;
                    }

                    const optimalPrice = calculateOptimalPrice(cost, targetMargin);

                    return {
                        id: variant.id,
                        price: optimalPrice,
                        is_enabled: variant.is_enabled
                    };
                });

                // ‰æ°Ê†º„ÅåÂ§âÊõ¥„Åï„Çå„Åü„ÅãÁ¢∫Ë™ç
                const hasChanges = updatedVariants.some((updatedVariant, index) => {
                    return updatedVariant.price !== variants[index].price;
                });

                if (!hasChanges) {
                    console.log(`‚úì ‰æ°Ê†º„ÅØÊó¢„Å´ÊúÄÈÅ©„Åß„Åô`);
                    skippedCount++;
                    updateDetails.push({
                        productId: product.id,
                        title: product.title,
                        productType: costInfo.name,
                        status: 'skipped',
                        reason: 'Already optimal pricing'
                    });
                    continue;
                }

                if (dryRun) {
                    console.log(`[DRY RUN] Êõ¥Êñ∞‰∫àÂÆö: ${costInfo.name}`);
                    updatedCount++;
                    updateDetails.push({
                        productId: product.id,
                        title: product.title,
                        productType: costInfo.name,
                        status: 'dry-run',
                        message: 'Êõ¥Êñ∞„ÅåÂøÖË¶ÅÔºàDRY RUN„ÅÆ„Åü„ÇÅÂÆüË°å„Å™„ÅóÔºâ',
                        variants: updatedVariants.map((v, i) => ({
                            title: variants[i].title,
                            currentPrice: variants[i].price,
                            newPrice: v.price
                        }))
                    });
                    continue;
                }

                // ÂïÜÂìÅ„ÇíÊõ¥Êñ∞
                const updateResponse = await fetch(
                    `https://api.printify.com/v1/shops/${shopId}/products/${product.id}.json`,
                    {
                        method: 'PUT',
                        headers: {
                            'Authorization': `Bearer ${apiKey}`,
                            'Content-Type': 'application/json'
                        },
                        body: JSON.stringify({
                            variants: updatedVariants
                        })
                    }
                );

                if (!updateResponse.ok) {
                    const errorText = await updateResponse.text();
                    console.error(`Failed to update product ${product.id}: ${errorText}`);
                    errorCount++;
                    updateDetails.push({
                        productId: product.id,
                        title: product.title,
                        productType: costInfo.name,
                        status: 'error',
                        reason: `Update failed: ${errorText.substring(0, 100)}`
                    });
                    continue;
                }

                console.log(`‚úÖ Êõ¥Êñ∞ÊàêÂäü: ${costInfo.name}`);
                updatedCount++;
                updateDetails.push({
                    productId: product.id,
                    title: product.title,
                    productType: costInfo.name,
                    status: 'updated',
                    message: `„Çµ„Ç§„Ç∫Âà•‰æ°Ê†º„ÇíÊúÄÈÅ©ÂåñÔºà${targetMargin}%Âà©ÁõäÁéáÈÅîÊàêÔºâ`
                });

                // „É¨„Éº„ÉàÂà∂ÈôêÂØæÁ≠ñ
                await new Promise(resolve => setTimeout(resolve, 500));

            } catch (error) {
                console.error(`Error processing product ${product.id}:`, error);
                errorCount++;
                updateDetails.push({
                    productId: product.id,
                    title: product.title,
                    status: 'error',
                    reason: error.message
                });
            }
        }

        console.log(`\nüìä Âá¶ÁêÜÂÆå‰∫Ü: Êõ¥Êñ∞${updatedCount}‰ª∂„ÄÅ„Çπ„Ç≠„ÉÉ„Éó${skippedCount}‰ª∂„ÄÅ„Ç®„É©„Éº${errorCount}‰ª∂`);

        res.status(200).json({
            success: true,
            dryRun: dryRun,
            targetMargin: targetMargin,
            updated: updatedCount,
            skipped: skippedCount,
            errors: errorCount,
            total: products.length,
            details: updateDetails,
            priceConfig: {
                'T„Ç∑„É£„ÉÑ (Gildan 5000)': {
                    'S-XL': '$9.99 (39.9%)',
                    '2XL': '$12.99 (38.4%)',
                    '3XL': '$16.99 (41.1%)'
                },
                '„Çπ„Ç¶„Çß„ÉÉ„Éà (Gildan 18000)': {
                    'S-XL': '$22.99 (39.1%)',
                    '2XL': '$27.99 (39.3%)',
                    '3XL': '$32.99 (39.4%)'
                },
                '„Éï„Éº„Éá„Ç£ (Gildan 18500)': {
                    'S-XL': '$27.99 (39.3%)',
                    '2XL': '$32.99 (39.4%)',
                    '3XL': '$37.99 (39.5%)'
                }
            },
            note: dryRun ? 'DRY RUN„É¢„Éº„Éâ„Åß„Åô„ÄÇÂÆüÈöõ„ÅÆÊõ¥Êñ∞„ÇíË°å„ÅÜ„Å´„ÅØdryRun=false„ÅßÂÜçÂÆüË°å„Åó„Å¶„Åè„Å†„Åï„ÅÑ„ÄÇ' : '„Çµ„Ç§„Ç∫Âà•‰æ°Ê†º„Çí$X.99ÂΩ¢Âºè„ÅßÊúÄÈÅ©Âåñ„Åó„Åæ„Åó„ÅüÔºàÂà©ÁõäÁéá38„Äú41%Ôºâ„ÄÇ'
        });

    } catch (error) {
        console.error('‚ùå ‰æ°Ê†ºÊõ¥Êñ∞„Ç®„É©„Éº:', error);
        throw error;
    }
}

// „É¨„Éº„ÉàÂà∂Èôê: 2„É™„ÇØ„Ç®„Çπ„Éà/ÂàÜÔºàÈáç„ÅÑÂá¶ÁêÜÔºâ
export default rateLimitMiddleware(
    asyncHandler(handler),
    { maxRequests: 2, windowMs: 60000 }
);
